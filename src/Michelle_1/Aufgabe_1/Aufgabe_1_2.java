package Michelle_1.Aufgabe_1;

public class Aufgabe_1_2 {
    public static void main(String[] args){
        int a = 1, b = 2, c = 3, d = 4;


        /*  Aufgabenteil (a)

         */

         //a + b / c - d;
        System.out.println(a + b / c - d);

        a = 1; b = 2; c = 3; d = 4; // reinitialisieren


        /* Aufgabenteil (b)

         */
        // a + ++a + a++;
        System.out.println("bla");
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a++);

        a = 1; b = 2; c = 3; d = 4; // reinitialisieren


        /* Aufgabenteil (c)

         */

        // c *= d /= ++a;
        System.out.println();

        a = 1; b = 2; c = 3; d = 4;  // reinitialisieren

        /*  Aufgabenteil (d)

         */

        // c != b + a || a + c > d;
        System.out.println();

        a = 1; b = 2; c = 3; d = 4;      //  reinitialisieren

        /* Aufgabenteil (e)

         */
        // (++a + ++b ==  d-- + --d) ? a : c;
        System.out.println();


    }
}

